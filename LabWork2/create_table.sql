

CREATE TABLE STUDENTS(id NUMBER, name VARCHAR2(100), group_id NUMBER);
CREATE TABLE GROUPS (
    ID NUMBER,
    NAME VARCHAR2(100),
    C_VAL NUMBER DEFAULT 0
);
CREATE TABLE STUDENTS_AUDIT(
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    STUDENT_ID NUMBER,
    ACTION VARCHAR(6),
    OLD_DATA VARCHAR(2000),
    NEW_DATA VARCHAR(2000),
    ACTION_DATE TIMESTAMP
);